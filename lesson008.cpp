#include <iostream>
using namespace std;

int main() {

    // ADDITION
    int a = 10;
    a = a + 5;      // classic addition
    cout << a << "\n";
    int b = 10;
    b += 5;         // shorthand addition
    cout << b << "\n";
    //-----------------------------------------

    // SUBTRACTION
    int c = 10;
    c = c - 3;      // classic subtraction
    cout << c << "\n";
    int d = 10;
    d -= 3;         // shorthand subtraction
    cout << d << "\n";
    //-----------------------------------------

    // MULTIPLICATION
    int e = 10;
    e = e * 2;      // classic multiplication
    cout << e << "\n";
    int f = 10;
    f *= 2;         // shorthand multiplication
    cout << f << "\n";
    //-----------------------------------------

    // DIVISION
    int g = 10;
    g = g / 2;      // classic division
    cout << g << "\n";
    int h = 10;
    h /= 2;         // shorthand division
    cout << h << "\n";
    //-----------------------------------------

    // MODULUS
    int i = 10;
    i = i % 3;      // classic modulus
    cout << i << "\n";
    int j = 10;
    j %= 3;         // shorthand modulus
    cout << j << "\n";
    //-----------------------------------------
}
